package com.example.colors2web.zummix_app.POJO.OrderEdit;

public class EditExpedite {

    String is_expedite;
    String updated_by;

    public EditExpedite(String is_expedite, String updated_by) {
        this.is_expedite = is_expedite;
        this.updated_by = updated_by;
    }

    public String getIs_expedite() {
        return is_expedite;
    }

    public void setIs_expedite(String is_expedite) {
        this.is_expedite = is_expedite;
    }

    public String getUpdated_by() {
        return updated_by;
    }

    public void setUpdated_by(String updated_by) {
        this.updated_by = updated_by;
    }
}
